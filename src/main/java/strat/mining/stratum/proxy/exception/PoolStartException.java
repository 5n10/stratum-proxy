package strat.mining.stratum.proxy.exception;

public class PoolStartException extends Exception {

	public PoolStartException() {
		super();
	}

	public PoolStartException(String message, Throwable cause) {
		super(message, cause);
	}

	public PoolStartException(String message) {
		super(message);
	}

	public PoolStartException(Throwable cause) {
		super(cause);
	}

}
