package strat.mining.stratum.proxy.exception;

public class UnsupportedPoolSwitchingStrategyException extends RuntimeException {

	public UnsupportedPoolSwitchingStrategyException() {
		super();
	}

	public UnsupportedPoolSwitchingStrategyException(String message, Throwable cause) {
		super(message, cause);
	}

	public UnsupportedPoolSwitchingStrategyException(String message) {
		super(message);
	}

	public UnsupportedPoolSwitchingStrategyException(Throwable cause) {
		super(cause);
	}

}
